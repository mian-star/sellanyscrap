import mongoose from "mongoose";

const ContactFormSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
  message: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Export model (avoiding model overwrite issue in development)
export const Contact =
  mongoose.models.contacts || mongoose.model("contacts", ContactFormSchema);
